# Q: 12. What is the difference between range(10), range(0, 10), and range(0, 10, 1) in a for loop?

print("for loop range(10) result")
for i in range(10):
	print(i)
print("for loop range(0,10) result")
for i in range(0,10):
	print(i)
print("for loop range(0,10,1) result")
for i in range(0,10,1):
	print(i)